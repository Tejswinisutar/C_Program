///Addition Function

void Add()
{
   int N1 = 0,N2 = 0,Res = 0;

   printf("\n Enter 1st No");
   scanf("%d",&N1);

   printf("\n Enter 2nd No");
   scanf("%d",&N2);

   Res = N1 + N2;

   printf("\n Addition of %d + %d = %d.",N1,N2,Res);

   return;
}

